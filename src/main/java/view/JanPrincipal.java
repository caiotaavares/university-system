    private void ButtonCadastrarDepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonCadastrarDepActionPerformed
        if (TextNomeDep.getText().isEmpty()) {
            mensagemTela(false, "Precisa de nome!");
        } else {
            try {
                Departamento departamento = new Departamento(TextNomeDep.getText());
                cbDepartamento.getLista().add(departamento);
                if (cbDepartamento.escrever(false)) {
                    mensagemTela(true, "Cadastrado!");
                } else {
                    mensagemTela(false, "Erro ao cadastrar!");
                    if (JOptionPane.showConfirmDialog(this, "Gostaria de cadastrar?", "Cadastro", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                        ButtonCadastrarDepActionPerformed(null);
                    }
                }
            } catch (Exception e) {
                mensagemTela(false, e.toString());
            } finally {
                TextNomeDep.setText("");
            }
        }
    }//GEN-LAST:event_ButtonCadastrarDepActionPerformed

