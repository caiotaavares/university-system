    private void jButtonCadTecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadTecActionPerformed
        // CADASTRAR TECNICO
        List<Departamento> dep = cbDepartamento.getLista();
        
        String nome = jTextFieldCadNomeTec.getText();
        String nivel = (String) jComboBoxCadNivelTec.getSelectedItem();
        String funcao = (String) jComboBoxCadFuncaoTec.getSelectedItem();
        int salario = Integer.parseInt(jTextFieldCadSalarioTec.getText());
        
        if (jTextFieldCadNomeTec.getText().isEmpty() || jTextFieldCadSalarioTec.getText().isEmpty()) {
           mensagemTela(false, "Todos os campos devem estar prechidos!");
           return;
        }
        
        String selectedDepartamento = (String) jComboBoxDep.getSelectedItem();
        for (Departamento departamento : dep) {
            if (departamento.getNome().equals(selectedDepartamento)) {
                Funcionario tec = new Tecnico(
                    nome,
                    salario,
                    nivel,
                    funcao
                );
                
                ControllerDepartamento cd = new ControllerDepartamento();
                cd.adicionarFuncionario(tec, departamento);
                
                if (cbDepartamento.escrever(false)) {
                    mensagemTela(true, "Cadastrado!");
                } else {
                    mensagemTela(false, "Erro ao cadastrar!");
                }
                mensagemTela(false, tec.toString());
                mensagemTela(false, departamento.toString());
            }
        }
    }//GEN-LAST:event_jButtonCadTecActionPerformed

